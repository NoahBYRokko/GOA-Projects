'''1) გამოიყენეთ capitalize, upper, lower, count და title მეთოდები და ახსენით ყველა მეთოდიუ როგორ მუშაობს + ახსენით რა განსხვავებაა მეთოდსა და ფუნქციას შორის'''

# Task1.

sentence = "simple sentence"
print(sentence.capitalize())

# capitalize method is used for converting the first character to upper case

# Task2.

sentence = "simple sentence"
print(sentence.upper())

# upper method is used for converting every character to upper case

# Task3.

sentence = "simple sentence"
print(sentence.lower())

# lower method is used for converting every character to lower case

# Task4.

sentence = "simple sentence"
print(sentence.count(" "))

# count method is used for counting the number of occurrences of a character

# Task5.

sentence = "simple sentence"
print(sentence.title())

# title method is used for converting the first character of each word to upper case